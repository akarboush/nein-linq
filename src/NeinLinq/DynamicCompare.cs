namespace NeinLinq;

/// <summary>
/// Define a comparison method.
/// </summary>
public enum DynamicCompare
{
    /// <summary>
    /// A equal comparison.
    /// </summary>
    Equal,

    /// <summary>
    /// A not equal comparison.
    /// </summary>
    NotEqual,

    /// <summary>
    /// A greater than comparison.
    /// </summary>
    GreaterThan,

    /// <summary>
    /// A greater than or equal comparison.
    /// </summary>
    GreaterThanOrEqual,

    /// <summary>
    /// A less than comparison.
    /// </summary>
    LessThan,

    /// <summary>
    /// A less than or equal comparison.
    /// </summary>
    LessThanOrEqual
}
